#[test]
fn test_b64_invalid_char() {
    let str = "I'm killing your brain like a poisonous mushroom";

    assert_eq!(
        DecodeError("".to_string()).type_id(),
        Base64::from_str(str).unwrap_err().type_id()
    )
}

