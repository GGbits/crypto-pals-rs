#[test]
fn test_hamming_distance() {
    let b1 = Bytes(b"this is a test".to_vec());
    let b2 = Bytes(b"wokka wokka!!!".to_vec());

    let result = b1.hamming_distance(&b2);
    assert_eq!(result, 37u32)
}

